print("Welcome to the rollercoaster!")
height = int(input("What is your height in cm? "))

# If Statements require the results to be indented. If they are not, you will get an indent error
# The left side - if <variable> is the Equality Check. The right side after the operator is the assignment
# If/Else is equiv to: "If the condition evaluates to True, do something, else do something else"
if height > 120:
    print("You can ride the rollercoaster")
else:
    print("Sorry, you have to be 120cm or more before you can ride.")

